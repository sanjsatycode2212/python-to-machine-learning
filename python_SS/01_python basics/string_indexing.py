## string indexing in python programming language.


## string is one type of data type which is use to store text or characters in it.
## string is a collection of multiple characters.

## strinf indexing means we can access each character of string using index number.
## in python programming language index number start from 0 to n-1 (where n is length of string).
## we can also use negative indexing in string to access characters from end of string.
## in negative indexing last character index number is -1 and first character index number is -n (where n is length of string).
## ex:->>>
string = "Hello, World!"  ## here string is variable name and "Hello, World!" is value stored in it.
print(string)  ## here string is variable name and "Hello, World!" is value stored in it.

# positive indexing
print(string[0])  ## H
print(string[7])  ## W
print(string[12]) ## !

# negative indexing
print(string[-1])  ## !
print(string[-6])  ## W
print(string[-13]) ## H

