## Comparison operators, also called relational operators, are used to compare two values

## Comparison operators will always provide Boolean result that is True and False.

# equal to        >>   ==
# not equal to    >>   !=
# greater than    >>   >
# less than       >>   <
# greater than or equal to  >>   >=
# less than or equal to     >>   <=

## now we will see each operator with example.

# equal to
a = 10
b = 20

c = (a == b)  ## ager a aur b equal honge to ye True dega warna False dega.
print("Equal to: ", c)  # Output: False

# not equal to

a = 10
b = 20
c = (a != b)  ## ager a aur b equal nahi honge to ye True dega warna False dega.
print("Not Equal to: ", c)  # Output: True

# greater than

a = 20
b = 10
c = (a > b)  ## ager a b se bada hoga to ye True dega warna False dega.
print("Greater Than: ", c)  # Output: True

# less than

a = 10
b = 20
c = (a < b)  ## ager a b se chota hoga to ye True dega warna False dega.
print("Less Than: ", c)  # Output: True

# greater than or equal to

a = 20
b = 20
c = (a >= b)  ## ager a b se bada hoga ya equal hoga to ye True dega warna False dega.
print("Greater Than or Equal To: ", c)  # Output: True

# less than or equal to

a = 10
b = 20
c = (a <= b)  ## ager a b se chota hoga ya equal hoga to ye True dega warna False dega.
print("Less Than or Equal To: ", c)  # Output: True


