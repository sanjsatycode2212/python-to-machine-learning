## assigment operator is a advanced version of arithmetic operators.

## ushme yahi operators hote hain jo arithmetic operators hote hain, bas farak itna hai ki 
## jo operators ko hum variable ko value assign karne ke liye use karte hain, unhe assignment operators kehte hain.


# addition assignment

a = 10
a += 20   # a = a + 20   ## isme bas diffrennce itna hi he ki yaha a += 20 ko advced form me likhte he hai jo ki a = a + 20 ke barabar hota hai.
print("Addition Assignment: ", a)  # Output: 30

# subtraction assignment

a = 20
a -= 10   # a = a - 10  ## isme bas diffrennce itna hi he ki yaha a -= 10 ko advced form me likhte he hai jo ki a = a - 10 ke barabar hota hai.
print("Subtraction Assignment: ", a)  # Output: 10

# multiplication assignment

a = 10
a *= 20   # a = a * 20  ## isme bas diffrennce itna hi he ki yaha a *= 20 ko advced form me likhte he hai jo ki a = a * 20 ke barabar hota hai.
print("Multiplication Assignment: ", a)  # Output: 200


# division assignment

a = 20
a /= 10   # a = a / 10  ## isme bas diffrennce itna hi he ki yaha a /= 10 ko advced form me likhte he hai jo ki a = a / 10 ke barabar hota hai.
print("Division Assignment: ", a)  # Output: 2.0

# modulus assignment

a = 20
a %= 3   # a = a % 3  ## isme bas diffrennce itna hi he ki yaha a %= 3 ko advced form me likhte he hai jo ki a = a % 3 ke barabar hota hai.
print("Modulus Assignment: ", a)  # Output: 2

# exponentiation assignment

a = 2
a **= 3   # a = a ** 3  ## isme bas diffrennce itna hi he ki yaha a **= 3 ko advced form me likhte he hai jo ki a = a ** 3 ke barabar hota hai.
print("Exponentiation Assignment: ", a)  # Output: 8

# floor division assignment

a = 20
a //= 3   # a = a // 3  ## isme bas diffrennce itna hi he ki yaha a //= 3 ko advced form me likhte he hai jo ki a = a // 3 ke barabar hota hai.
print("Floor Division Assignment: ", a)  # Output: 6

