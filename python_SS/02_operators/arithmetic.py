## there are 7 types of arithemtic operations in python.
# addition        >>   +
# subtraction     >>   -
# multiplication  >>   *
# division        >>   /
# modulus         >>   %
# exponentiation  >>   **
# floor division  >>   //


## now we will see each operation with example.

# addition

a = 10
b = 20
c = a+b
print("Addition: ", c)  # Output: 30


# subtraction

a = 20
b = 10
c = a-b
print("Subtraction: ", c)  # Output: 10

# multiplication
a = 10
b = 20
c = a*b
print("Multiplication: ", c)  # Output: 200


# division

a = 20
b = 10
c = a/b
print("Division: ", c)  # Output: 2.0


# modulus   :>> modules gives the remainder after division

a = 20
b = 3
c = a%b
print("Modulus: ", c)  # Output: 2

# exponentiation   :>>  exponentiation raises the number to the power of another number

a = 2
b = 3
c = a**b
print("Exponentiation: ", c)  # Output: 8


# floor division   :>>  floor division gives the largest integer less than or equal to the division result.

a = 20
b = 3
c = a//b
print("Floor Division: ", c)  # Output: 6



